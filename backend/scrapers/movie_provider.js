/**
 * Provider System Controller
 * Handles provider registration and the failover aggregation engine.
 */
class ProviderSystem {
    constructor() {
        this.providers = [];
    }

    registerProvider(provider) {
        this.providers.push(provider);
    }

    async resolveMedia(id) {
        console.log(`[System] Resolving media for ID: ${id}`);
        // Iterate through all providers linearly
        for (const provider of this.providers) {
            const rawUrl = await provider.extractStream(id);
            
            if (rawUrl) {
                console.log(`[System] Success with provider: ${provider.identifier}`);
                // Run through ad-killer middleware
                const safeUrl = AdKillerMiddleware.cleanUrl(rawUrl);
                
                return {
                    success: true,
                    provider: provider.identifier,
                    stream: safeUrl,
                    subtitles: [
                        { lang: 'English', url: 'https://example.com/subs/en.vtt' },
                        { lang: 'Spanish', url: 'https://example.com/subs/es.vtt' }
                    ]
                };
            }
        }
        
        return {
            success: false,
            error: "All registered extractors failed or source is dead."
        };
    }
}

