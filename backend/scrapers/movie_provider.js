const axios = require('axios');
const { HttpsProxyAgent } = require('https-proxy-agent');
const ProxyManager = require('../services/proxy_manager');

/**
 * Executes Axios calls wrapped in residential proxies with auto-rotation.
 */
async function fetchWithRetry(url, region = 'US', maxRetries = 3) {
    let attempt = 0;
    while (attempt < maxRetries) {
        attempt++;
        const proxyUrl = ProxyManager.getProxyByRegion(region);
        const headers = ProxyManager.getSpoofedHeaders(); // Inject realistic headers
        
        console.log(`[Fetch Attempt ${attempt}] Routing through proxy: ${proxyUrl}`);
        try {
            const agent = new HttpsProxyAgent(proxyUrl);
            const response = await axios.get(url, {
                httpsAgent: agent,
                headers: headers,
                timeout: 5000
            });
            return response.data;
        } catch (error) {
            console.warn(`[Proxy Error] Attempt ${attempt} failed: ${error.message}`);
            if (error.response && error.response.status === 403) {
                console.warn(`[Proxy Blocked] 403 Forbidden detected. Immediate IP rotation...`);
            }
            if (attempt === maxRetries) throw new Error("Max proxy retries exceeded.");
        }
    }
}

/**
 * The 'Ad-Killer' Middleware
 * Strips URLs of parameters containing 'ads', 'redirect', or 'popup'.
 */
class AdKillerMiddleware {
    static cleanUrl(rawUrl) {
        if (!rawUrl) return null;
        try {
            const urlObj = new URL(rawUrl);
            const paramsToKeep = new URLSearchParams();

            for (const [key, value] of urlObj.searchParams.entries()) {
                const lowerKey = key.toLowerCase();
                const lowerVal = value.toLowerCase();
                if (
                    lowerKey.includes('ad') || lowerVal.includes('ad') ||
                    lowerKey.includes('redirect') || lowerVal.includes('redirect') ||
                    lowerKey.includes('popup') || lowerVal.includes('popup')
                ) {
                    console.log(`[AdKiller] Blocked parameter => ${key}=${value}`);
                    continue; // Skip malicious ad-tracking/redirect parameters
                }
                paramsToKeep.append(key, value);
            }
            
            // Reconstruct the clean URL
            urlObj.search = paramsToKeep.toString();
            console.log(`[AdKiller] Cleaned URL => ${urlObj.toString()}`);
            return urlObj.toString();
        } catch (e) {
            console.error('[AdKiller] Error parsing URL:', e.message);
            return rawUrl;
        }
    }
}

